public class AssignmentOperators {
    public static void main(String[] args) {
        int p = 10;
        int q;


        // for =
        q = p;
        System.out.println(q); //300

        // for +=
        p += q; //p = p+q
        System.out.println(p);

        // for -=
        p -= q; //p = p-q
        System.out.println(p);

        // for *=
        p *= q; //p = p*q
        System.out.println(p);

        // for /=
        p /= q; //p = p/q
        System.out.println(p);
    }
}
